name = "University"

# For Loop
print('For Loop');
for x in range(10):
    print(name, " ")

# While loop
print('While Loop');
count = 0
while count < 10:
    print(name, "")
    count += 1


# Do While loop
print('Do while loop')
count1 = 0
while count1 < 10:
    print(name, "")
    count1 += 1
if count1 > 10:
        exit(0)

